using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace LibCalculator
{
    public class Calculator
    {
        public double Add(double nr1, double nr2)
        {
            return nr1 + nr2;
        }
        public double Substract(double nr1, double nr2)
        {
            return nr1 - nr2;
        }
        public double Multiplay(double nr1, double nr2)
        {
            return nr1 * nr2;
        }
        public double Devide(double nr1, double nr2)
        {
            return nr1 / nr2;
        }
    }
}
