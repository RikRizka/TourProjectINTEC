        public async Task<IActionResult> Index()
        {
            var productCategoryViewModel = _context.Products
                  .Include(b => b.Category)
                  .Select(b => new ProductCategoryViweModel
                  {
                      ProductId= b.ProductId,
                       ProductName = b.ProductName,
                      Name = b.Category.Name,
                  });

            return View(productCategoryViewModel);
        }

          public async Task<IActionResult> Filters(int? GeselecteerdeCategoryId)
        {
            var categories = await _context.Categories.ToListAsync();

            IQueryable<Product> pruductsQuery = _context.Products.Include(b => b.Category);

            if (GeselecteerdeCategoryId.HasValue)
            {
                pruductsQuery = pruductsQuery.Where(b => b.CategoryId == GeselecteerdeCategoryId);
            }

            var products = await pruductsQuery.ToListAsync();

            var productsViewModel = products.Select(b => new ProductCategoryViweModel
            {
                ProductId = b.ProductId,
                ProductName = b.ProductName,
                Name = b.Category.Name
            }).ToList();

            var filtersViewModel = new ProductsViewModel
            {
                Categories = categories,
              Products = productsViewModel,
                GeselecteerdCategoriesId= GeselecteerdeCategoryId ?? 0
            };
            return View(filtersViewModel);

